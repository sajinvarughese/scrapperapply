package org.scrappers.bean;

public class UserDetails {
	private String fullName;
	private String emailId;
	private String mobileNumber;
	private String dateOfBirth;
	private String country;
	private String state;
	private String city;
	private String address;
	private String experienceMonth;
	private String experienceYear;
	private String currentSalary;
	private String expectedSalary;
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getExperienceMonth() {
		return experienceMonth;
	}
	public void setExperienceMonth(String experienceMonth) {
		this.experienceMonth = experienceMonth;
	}
	public String getExperienceYear() {
		return experienceYear;
	}
	public void setExperienceYear(String experienceYear) {
		this.experienceYear = experienceYear;
	}
	public String getCurrentSalary() {
		return currentSalary;
	}
	public void setCurrentSalary(String currentSalary) {
		this.currentSalary = currentSalary;
	}
	public String getExpectedSalary() {
		return expectedSalary;
	}
	public void setExpectedSalary(String expectedSalary) {
		this.expectedSalary = expectedSalary;
	}

}
